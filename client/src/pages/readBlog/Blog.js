import Header from "../../components/header/Header"
import SingleBlog from "../../components/singleBlog/SingleBlog"
import Comment from "../../components/comment/Comment"
import AddComment from "../../components/addComment/AddComment"
import { useLocation } from "react-router-dom";
import { useEffect, useState } from "react";
import axios from "axios"
import { api } from "../../backend";
import "./blog.css"

export default function Blog() {
  const location = useLocation();
  const path = location.pathname.split("/")[2];
  const [post, setpost] = useState([]);
  const [comments, setComments] = useState([]);

  useEffect(()=>{
    const fetchPosts = async () => {
      const res = await axios.get(api+"/post/"+path)
      setpost(res.data.data)
    }
    fetchPosts()
  }, [path])
  
  useEffect(()=>{
    const fetchComments = async () => {
      const res = await axios.get(api+"/comment/"+path)
      setComments(res.data.data)
    }
    fetchComments()
  }, [path])
  return (
    <div>
      <Header />
      <SingleBlog post={post}/>
      <div className="like-comment">
        <div className="like-cmt-icon">
          <div className="likes-icon">
            <svg viewBox="0 0 1024 1024" height="1.4em" xmlns="http://www.w3.org/2000/svg"><path d="M885.9 533.7c16.8-22.2 26.1-49.4 26.1-77.7 0-44.9-25.1-87.4-65.5-111.1a67.67 67.67 0 0 0-34.3-9.3H572.4l6-122.9c1.4-29.7-9.1-57.9-29.5-79.4A106.62 106.62 0 0 0 471 99.9c-52 0-98 35-111.8 85.1l-85.9 311H144c-17.7 0-32 14.3-32 32v364c0 17.7 14.3 32 32 32h601.3c9.2 0 18.2-1.8 26.5-5.4 47.6-20.3 78.3-66.8 78.3-118.4 0-12.6-1.8-25-5.4-37 16.8-22.2 26.1-49.4 26.1-77.7 0-12.6-1.8-25-5.4-37 16.8-22.2 26.1-49.4 26.1-77.7-.2-12.6-2-25.1-5.6-37.1zM184 852V568h81v284h-81zm636.4-353l-21.9 19 13.9 25.4a56.2 56.2 0 0 1 6.9 27.3c0 16.5-7.2 32.2-19.6 43l-21.9 19 13.9 25.4a56.2 56.2 0 0 1 6.9 27.3c0 16.5-7.2 32.2-19.6 43l-21.9 19 13.9 25.4a56.2 56.2 0 0 1 6.9 27.3c0 22.4-13.2 42.6-33.6 51.8H329V564.8l99.5-360.5a44.1 44.1 0 0 1 42.2-32.3c7.6 0 15.1 2.2 21.1 6.7 9.9 7.4 15.2 18.6 14.6 30.5l-9.6 198.4h314.4C829 418.5 840 436.9 840 456c0 16.5-7.2 32.1-19.6 43z"></path></svg>
            <span className="likes">Likes</span>
          </div>
          <div className="cmts-icon">
            <svg viewBox="0 0 1024 1024" version="1.1" height="1.4em" xmlns="http://www.w3.org/2000/svg"><defs></defs><path d="M573 421c-23.1 0-41 17.9-41 40s17.9 40 41 40c21.1 0 39-17.9 39-40s-17.9-40-39-40zM293 421c-23.1 0-41 17.9-41 40s17.9 40 41 40c21.1 0 39-17.9 39-40s-17.9-40-39-40z"></path><path d="M894 345c-48.1-66-115.3-110.1-189-130v0.1c-17.1-19-36.4-36.5-58-52.1-163.7-119-393.5-82.7-513 81-96.3 133-92.2 311.9 6 439l0.8 132.6c0 3.2 0.5 6.4 1.5 9.4 5.3 16.9 23.3 26.2 40.1 20.9L309 806c33.5 11.9 68.1 18.7 102.5 20.6l-0.5 0.4c89.1 64.9 205.9 84.4 313 49l127.1 41.4c3.2 1 6.5 1.6 9.9 1.6 17.7 0 32-14.3 32-32V753c88.1-119.6 90.4-284.9 1-408zM323 735l-12-5-99 31-1-104-8-9c-84.6-103.2-90.2-251.9-11-361 96.4-132.2 281.2-161.4 413-66 132.2 96.1 161.5 280.6 66 412-80.1 109.9-223.5 150.5-348 102z m505-17l-8 10 1 104-98-33-12 5c-56 20.8-115.7 22.5-171 7l-0.2-0.1C613.7 788.2 680.7 742.2 729 676c76.4-105.3 88.8-237.6 44.4-350.4l0.6 0.4c23 16.5 44.1 37.1 62 62 72.6 99.6 68.5 235.2-8 330z"></path><path d="M433 421c-23.1 0-41 17.9-41 40s17.9 40 41 40c21.1 0 39-17.9 39-40s-17.9-40-39-40z"></path></svg>
            <span className="cmts">{comments.length} Comments</span>
          </div>
        </div>
        <h3 className="underline">Comments</h3>
        {comments.length>0?(
            comments.map(c => <Comment key={c._id} comment={c} />)
          ) : (
            <p>No Comments Yet</p>
          )
        }
        <AddComment />
      </div>
    </div>
  )
}
